function sleep(ms: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, ms))
}

/**
 * Phase 12 Stage 4: emit tool_start / tool_end events around any tool
 * dispatch so the Ink AgentStream can render a <ToolCard> for each one.
 *
 * No-op in legacy console mode (the existing console.log lines below
 * still carry the same info for that path). When `runFn` throws we
 * still emit the matching tool_end with ok=false so the card transitions
 * to the error state — then re-throw so the existing error handling
 * upstream is unaffected.
 *
 * The `id` is generated by the caller via `nextToolEventId()` so the
 * agent loop can pair start/end deterministically (and we don't tie
 * tool ids to the tool name, which collides on repeated calls).
 */
let toolEventCounter = 0
function nextToolEventId(): string {
  toolEventCounter += 1
  return `t${toolEventCounter}`
}

async function surfaceToolCall<T>(
  id: string,
  tool: string,
  label: string,
  args: Record<string, unknown> | undefined,
  runFn: () => Promise<T>,
): Promise<T> {
  if (!isInkActive()) return runFn()
  emitAgent({ type: "tool_start", id, tool, label, args })
  try {
    const out = await runFn()
    emitAgent({ type: "tool_end", id, ok: true })
    return out
  } catch (err) {
    emitAgent({ type: "tool_end", id, ok: false, error: (err as Error).message })
    throw err
  }
}

/**
 * Phase 12 Stage 4 batch variant: emit one tool_start per tool in the
 * batch BEFORE the batch runs, then end ALL with the same outcome
 * (the batch executor doesn't return per-tool failures via a thrown
 * error — the per-tool outcome lives in the response payload, which
 * is processed separately upstream). If the entire batch throws, all
 * cards transition to error.
 */
async function surfaceToolBatch<T>(
  toolCalls: ReadonlyArray<{ tool: string; args: Record<string, unknown> }>,
  runFn: () => Promise<T>,
): Promise<T> {
  if (!isInkActive()) return runFn()
  const ids = toolCalls.map((tc) => {
    const id = nextToolEventId()
    const label = formatToolLabel(tc.tool, tc.args) ?? tc.tool
    // Phase 14 Stage 2: include args so the new ActionCard can derive
    // its own Verb(target) header on the cli side. Existing label still
    // emitted for back-compat with consumers that already use it.
    emitAgent({ type: "tool_start", id, tool: tc.tool, label, args: tc.args })
    return id
  })
  try {
    const out = await runFn()
    for (const id of ids) emitAgent({ type: "tool_end", id, ok: true })
    return out
  } catch (err) {
    const msg = (err as Error).message
    for (const id of ids) emitAgent({ type: "tool_end", id, ok: false, error: msg })
    throw err
  }
}

/**
 * Phase 11 Stage 5: one-line copy describing a transition between awareness
 * states. Returns null when the transition isn't worth surfacing (e.g.
 * recovery from off_course → on_track is a positive signal but doesn't need
 * its own line — the badge flip in the next chunk-progress render carries it).
 */
function describeAwarenessTransition(
  from: "on_track" | "off_course" | "blocked",
  to: "on_track" | "off_course" | "blocked",
): string | null {
  if (from === to) return null
  if (from === "on_track" && to === "off_course") {
    return colors.warning("⚠ ") + colors.muted("confidence dipped — watching for drift")
  }
  if (to === "blocked") {
    // The off-course modal will appear immediately after this; this line
    // tees it up so the user sees why the prompt is interrupting them.
    return colors.error("✖ ") + colors.muted("blocked — handing off")
  }
  // Recoveries (e.g. blocked → on_track after a redirect) are surfaced via
  // the cooldown skip log; no extra line needed.
  return null
}

/**
 * Spinner shim: lets the existing `spinner.text = …` / `spinner.start()` /
 * `spinner.stop()` callsites work whether we're driving an `ora` instance
 * (legacy console mode) or emitting events to the Ink `<AgentStream>`.
 *
 * In Ink mode we deliberately don't render anything to the terminal — the
 * AgentStream component owns the visible region. Without this, ora and Ink
 * would both draw a "thinking..." line at the same position.
 */
type Ora = ReturnType<typeof ora>

function createSpinner(): Ora {
  if (isInkActive()) {
    // Initial text is empty so the Ink <RichSpinner> takes over with its
    // own ~22-verb cycle (`thinking… hmm… debugging… searching…`). The
    // agent overrides this with specific labels (server phase events,
    // tool prep, etc.) via `spinner.text =` calls below; whenever those
    // clear out we fall back to the cycle instead of repeating a single
    // static `thinking…` for the duration of a long pause.
    let current = ""
    emitAgent({ type: "spinner", text: current })
    // Stub mimicking the slice of the ora interface the agent + diff-preview
    // listener actually use. Anything unimplemented (succeed/render/clear/…)
    // returns the stub so chained calls don't blow up.
    const stub = {
      get text() { return current },
      set text(v: string) { current = v; emitAgent({ type: "spinner", text: v }) },
      start(text?: string) { if (text) current = text; emitAgent({ type: "spinner", text: current }); return stub },
      stop() { emitAgent({ type: "spinner_stop" }); return stub },
      fail(text?: string) {
        emitAgent({ type: "spinner_stop" })
        if (text) emitAgent({ type: "log", level: "error", text })
        return stub
      },
      succeed() { emitAgent({ type: "spinner_stop" }); return stub },
      warn() { emitAgent({ type: "spinner_stop" }); return stub },
      info() { emitAgent({ type: "spinner_stop" }); return stub },
      clear() { return stub },
      render() { return stub },
      isSpinning: true,
    }
    return stub as unknown as Ora
  }
  return ora({
    text: colors.muted("thinking..."),
    prefixText: "  ",
    spinner: "dots",
    color: "magenta",
  }).start()
}

